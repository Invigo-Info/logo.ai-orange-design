// Each category fills a 24-logo grid (live site renders 12 + 12 = 24; the
// gallery collapses to 12 by default with a "show more" toggle). Categories
// with fewer real assets cycle their set to reach the target.
const TARGET_LOGO_COUNT = 24;

/** Logos for a category — its own set when available, else the sample set,
 *  padded/trimmed to TARGET_LOGO_COUNT. */
export const getCategoryImages = (key: string): string[] => {
  const base = CATEGORY_IMAGES[key] ?? FALLBACK_LOGOS;
  if (base.length >= TARGET_LOGO_COUNT) return base.slice(0, TARGET_LOGO_COUNT);
  return Array.from(
    { length: TARGET_LOGO_COUNT },
    (_, i) => base[i % base.length],
  );
};

